package main;

public class Main {

	public static void main(String[] args) {
		
		System.out.println("===== 반복문 =====");
		
		// 무한반복 가능
		// n번 만큼 반복작업 가능
		
		// while
		int cnt = 0;		// 초기식
		while(cnt < 10) {	// 조건식
			System.out.println("Hello World ~");
			cnt = cnt+1;	// 증감식
		}
		

	}

}












