package codeup;

public class CodeupMain00 {

	public static void main(String[] args) {
		
//		정수형 변수 x 에 값을 입력하고,
//		x가 양수인지 음수인지 0 인지 판단하여 출력
		
		int x = 3;
		
		if(x > 0) {
			System.out.println("양수");
		}else if(x == 0) {
			System.out.println("ZERO");
		}else {
			System.out.println("음수");
		}
		
		

	}

}
