package codeup;

public class CodeupMain04 {

	public static void main(String[] args) {
		
//		정수타입 변수 x에 값을 입력하고, 
//		x 가 홀수인지 짝수인지 판단하여 출력
		
		int x = 0;
		
		
		if(x == 0) {
			System.out.println("0");
		}else if(x % 2 == 1) {
			System.out.println("홀수");
		}else {
			System.out.println("짝수");
		}
		
	}

}
